# 百变七巧板
import turtle as t
t.setup(800, 500)
t.bgpic('背景图.png')

#!!!!!!!!!!!!!!!!!!!以下衣服和乌龟用于项目展示!!!!!!!!!!!!!!!!!!!#
# 以下七块拼成了一个完整的乌龟。
# 第一块
p = t.Pen()
p.color('red')
p.penup()
p.goto(0, 0)
p.pendown()
p.begin_fill()
p.goto(100*1.414, 0)
p.goto(0, 100*1.414)
p.end_fill()
# 第二块
p.color('orange')
p.penup()
p.goto(0, 100*1.414)
p.pendown()
p.begin_fill()
p.goto(100*1.414, 100*1.414)
p.goto(100*1.414, 0)
p.end_fill()
# 第三块
p.color('yellow')
p.penup()
p.goto(100*1.414, 0)
p.pendown()
p.begin_fill()
p.goto(100*1.414, 100*1.414)
p.goto(100*1.414+50*1.414, 50*1.414)
p.end_fill()
# 第四块
p.color('green')
p.penup()
p.goto(100*1.414, 0)
p.pendown()
p.begin_fill()
p.goto(100*1.414-25*1.414, -25*1.414)
p.goto(100*1.414+25*1.414, -25*1.414)
p.end_fill()
# 第五块
p.color('cyan')
p.penup()
p.goto(0, 0)
p.pendown()
p.begin_fill()
p.goto(-25*1.414, -25*1.414)
p.goto(25*1.414, -25*1.414)
p.end_fill()
# 第六块
p.color('blue')
p.penup()
p.goto(0, 0)
p.pendown()
p.begin_fill()
p.goto(0, 50*1.414)
p.goto(-50*1.414, 100*1.414)
p.goto(-50*1.414, 50*1.414)
p.end_fill()
# 第七块
p.color('purple')
p.penup()
p.goto(-50*1.414, 100*1.414)
p.pendown()
p.begin_fill()
p.goto(-100*1.414, 100*1.414)
p.goto(-100*1.414, 50*1.414)
p.goto(-50*1.414, 50*1.414)
p.end_fill()

p.hideturtle()
t.done()